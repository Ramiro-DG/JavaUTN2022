package ej1;

public class Artist {
	private String name;
	private Double hourlyCost;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Double getHourlyCost() {
		return hourlyCost;
	}
	public void setHourlyCost(Double hourlyCost) {
		this.hourlyCost = hourlyCost;
	}
	
}
